"use client";

import * as React from "react";
import * as SelectPrimitive from "@radix-ui/react-select";
import { Check, ChevronDown, ChevronUp } from "lucide-react";
import { cn } from "@/lib/utils";

const Select = SelectPrimitive.Root;
const SelectGroup = SelectPrimitive.Group;
const SelectValue = SelectPrimitive.Value;

const SelectTrigger = React.forwardRef(({ className, children, ...props }, ref) => (
  <SelectPrimitive.Trigger
    ref={ref}
    className={cn(
      "flex h-[50px] w-full items-center justify-between rounded-[14px] border border-[#d2d2d7] bg-white px-4 py-2 text-[15px] shadow-sm ring-offset-white placeholder:text-[#86868b] focus:outline-none focus:ring-[4px] focus:ring-[#0071e3]/10 focus:border-[#0071e3] disabled:cursor-not-allowed disabled:opacity-50 dark:border-[#424245] dark:bg-[#1d1d1f] dark:ring-offset-[#1c1c1e] dark:placeholder:text-[#98989d] dark:focus:ring-[#2997ff]/20 dark:focus:border-[#2997ff] [&>span]:line-clamp-1",
      className
    )}
    {...props}
  >
    {children}
    <SelectPrimitive.Icon asChild>
      <ChevronDown className="h-4 w-4 opacity-50 text-[#86868b]" />
    </SelectPrimitive.Icon>
  </SelectPrimitive.Trigger>
));
